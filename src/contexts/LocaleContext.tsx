function detectCountry(): Locale {
  try {
    const tz = Intl.DateTimeFormat().resolvedOptions().timeZone;
    if (tz && tz.startsWith("Europe/Istanbul")) return "tr";
    const lang = navigator.language || "";
    if (lang.startsWith("tr")) return "tr";
  } catch {
    // fallback
  }
  return "en";
}

const LocaleContext = createContext<LocaleContextType | null>(null);

export function LocaleProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => {
    const saved = localStorage.getItem("khell_locale") as Locale | null;
    if (saved === "tr" || saved === "en") return saved;
    return detectCountry();
  });

  const setLocale = (l: Locale) => {
    setLocaleState(l);
    localStorage.setItem("khell_locale", l);
  };

  const t = (key: string): string => {
    return translations[locale][key] ?? key;
  };

  const currencySymbol = locale === "tr" ? "₺" : "$";

  const currency = (val: number): string => {
    if (locale === "tr") return `${val.toLocaleString("tr-TR", { minimumFractionDigits: 2, maximumFractionDigits: 2 })}₺`;
    return `$${val.toFixed(2)}`;
  };

  return (
    <LocaleContext.Provider value={{ locale, setLocale, t, currency, currencySymbol }}>
      {children}
    </LocaleContext.Provider>
  );
}

export function useLocale() {
  const ctx = useContext(LocaleContext);
  if (!ctx) throw new Error("useLocale must be used within LocaleProvider");
  return ctx;
}
