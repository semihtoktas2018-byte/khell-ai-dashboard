const TZ_TO_COUNTRY: Record<string, string> = {
  "Europe/Istanbul": "TR",
  "Europe/Paris": "FR",
  "Europe/Amsterdam": "NL",
  "Europe/Berlin": "DE",
  "Europe/London": "UK",
  "America/New_York": "US",
  "America/Chicago": "US",
  "America/Denver": "US",
  "America/Los_Angeles": "US",
};

function detectCountryCode(): string {
  try {
    const tz = Intl.DateTimeFormat().resolvedOptions().timeZone;
    if (tz && TZ_TO_COUNTRY[tz]) return TZ_TO_COUNTRY[tz];
    if (tz && tz.startsWith("America/")) return "US";
    if (tz && tz.startsWith("Europe/")) return "UK";
  } catch {}
  return "US";
}

function detectCountry(): Locale {
  try {
    const code = detectCountryCode();
    if (code === "TR") return "tr";
    if (code === "FR") return "fr";
    const lang = navigator.language || "";
    if (lang.startsWith("tr")) return "tr";
    if (lang.startsWith("fr")) return "fr";
  } catch {}
  return "en";
}

const LocaleContext = createContext<LocaleContextType | null>(null);

export function LocaleProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => {
    const saved = localStorage.getItem("khell_locale") as Locale | null;
    if (saved === "tr" || saved === "en" || saved === "fr") return saved;
    return detectCountry();
  });

  const [country, setCountry] = useState<string>(() => {
    try {
      const saved = localStorage.getItem("khell_country");
      if (saved) return saved;
    } catch {}
    return detectCountryCode();
  });

  useEffect(() => {
    try {
      const saved = localStorage.getItem("khell_country");
      if (saved) return;
    } catch {}

    fetch("https://ipapi.co/json/")
      .then((res) => res.json())
      .then((data) => {
        const code = data?.country_code;
        if (typeof code === "string" && code.length === 2) {
          setCountry(code);
          try {
            localStorage.setItem("khell_country", code);
          } catch {}
        }
      })
      .catch(() => {});
  }, []);

  const RATE_CACHE_KEY = "khell_usd_try_rate";
  const RATE_CACHE_TIME_KEY = "khell_usd_try_rate_time";
  const FALLBACK_RATE = 45;
  const CACHE_DURATION = 1000 * 60 * 60 * 12;

  const [usdToTry, setUsdToTry] = useState<number>(() => {
    try {
      const cached = localStorage.getItem(RATE_CACHE_KEY);
      return cached ? parseFloat(cached) : FALLBACK_RATE;
    } catch {
      return FALLBACK_RATE;
    }
  });

  useEffect(() => {
    try {
      const cachedTime = localStorage.getItem(RATE_CACHE_TIME_KEY);
      const isStale = !cachedTime || Date.now() - Number(cachedTime) > CACHE_DURATION;
      if (!isStale) return;
    } catch {}

    fetch("https://api.frankfurter.app/latest?from=USD&to=TRY")
      .then((res) => res.json())
      .then((data) => {
        const rate = data?.rates?.TRY;
        if (typeof rate === "number" && rate > 0) {
          setUsdToTry(rate);
          try {
            localStorage.setItem(RATE_CACHE_KEY, String(rate));
            localStorage.setItem(RATE_CACHE_TIME_KEY, String(Date.now()));
          } catch {}
        }
      })
      .catch(() => {});
  }, []);

  const setLocale = (l: Locale) => {
    setLocaleState(l);
    localStorage.setItem("khell_locale", l);
  };

  const t = (key: string): string => translations[locale][key] ?? translations["en"][key] ?? key;
  const currencySymbol = locale === "tr" ? "₺" : locale === "fr" ? "€" : "$";

  const currency = (val: number): string => {
    if (locale === "tr") {
      const tryVal = val * usdToTry;
      return `₺${tryVal.toLocaleString("tr-TR", { minimumFractionDigits: 2, maximumFractionDigits: 2 })}`;
    }
    if (locale === "fr") {
      return `${val.toFixed(2)} €`;
    }
    return `$${val.toFixed(2)}`;
  };

  return (
    <LocaleContext.Provider value={{ locale, setLocale, t, currency, currencySymbol, country }}>
      {children}
    </LocaleContext.Provider>
  );
}

export function useLocale() {
  const ctx = useContext(LocaleContext);
  if (!ctx) throw new Error("useLocale must be used within LocaleProvider");
  return ctx;
}
